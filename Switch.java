public class Switch{
 public static void main(String args[]){

  int opcion = 2;

  switch (opcion) {
    
    case 1: System.out.println("Opcion 1");
	break;
    case 2: System.out.println("Opcion 2");
	break;
    case 3: System.out.println("Opcion 3");
	break;
    default:
      System.out.print("Error");


  }

 }

}